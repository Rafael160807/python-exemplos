import random
aluno=['Rafael', 'Joao', 'Lucas', 'Gabriel', 'Mateus', 'Mariana']
# Embaralhar a lista
random.shuffle(aluno)
print(f"Lista Embaralhada: {aluno}")
# Ordena a lista crescente
aluno.sort()
print(f"Lista crescente: {aluno}")
# Ordena lista decrescente
aluno.sort(reverse=true)
print(f"Lista decrescente: {aluno}")