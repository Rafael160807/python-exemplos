alunos=['Adriana', 'João', 'Neto', 'Gabriel', 'Mateus', 'Natalia', 'Adriana']
print(f"Lista de alunos: {alunos}")
# Metodo Remove()
alunos.remove('Adriana')
print(f"Lista de alunos: {alunos}")
# Metodo pop()
alunos.pop(2)
print(f"Lista de alunos: {alunos}")
# Função del
del alunos[3]
print(f"Lista de alunos: {alunos}")